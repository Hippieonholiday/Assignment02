let lang = prompt("Enter a language code");
switch (lang) {
    case "en":
        alert ("Hello World");
        break;
    case "es":
        alert ("Hola Mundo");
        break;
    case "de":
        alert ("Hallo Welt");
        break;
    case "fr":
        alert ("Bonjour Le Monde");
        break;
    default:
        alert ("Hello World");
}